package main.model;

import java.util.Date;

public class Funcionario {
	
	String nome;
	int senha;
	int cpf;
	Date dt_admissao;
	String cargo;
	int vale;
	int valet;
	int psaude;
	int diretor;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getSenha() {
		return senha;
	}
	public void setSenha(int senha) {
		this.senha = senha;
	}
	public int getCpf() {
		return cpf;
	}
	public void setCpf(int cpf) {
		this.cpf = cpf;
	}
	public Date getDt_admissao() {
		return dt_admissao;
	}
	public void setDt_admissao(Date dt_admissao) {
		this.dt_admissao = dt_admissao;
	}
	public String getCargo() {
		return cargo;
	}
	public void setCargo(String cargo) {
		this.cargo = cargo;
	}
	public int getVale() {
		return vale;
	}
	public void setVale(int vale) {
		this.vale = vale;
	}
	public int getValet() {
		return valet;
	}
	public void setValet(int valet) {
		this.valet = valet;
	}
	public int getPsaude() {
		return psaude;
	}
	public void setPsaude(int psaude) {
		this.psaude = psaude;
	}
	public int getDiretor() {
		return diretor;
	}
	public void setDiretor(int diretor) {
		this.diretor = diretor;
	}
	
	
	
}
	